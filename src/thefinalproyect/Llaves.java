package thefinalproyect;


public class Llaves {
    int llave;
    int ubicacion;
    String demas;

    public Llaves() {
    }

    public Llaves(int llave, int ubicacion, String demas) {
        this.llave = llave;
        this.ubicacion = ubicacion;
        this.demas = demas;
    }

    public int getLlave() {
        return llave;
    }

    public void setLlave(int llave) {
        this.llave = llave;
    }

    public int getUbicacion() {
        return ubicacion;
    }

    public void setUbicacion(int ubicacion) {
        this.ubicacion = ubicacion;
    }

    public String getDemas() {
        return demas;
    }

    public void setDemas(String demas) {
        this.demas = demas;
    }

    
    
}
